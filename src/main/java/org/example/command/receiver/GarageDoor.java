package org.example.command.receiver;

public class GarageDoor {

    public void up() {
        System.out.println("Door is up");
    }

    public void down() {
        System.out.println("Door is down");
    }

    public void stop() {
        System.out.println("Door is stop");
    }

    public void lightOn() {
        System.out.println("Light is on in the garage door");
    }

    public void lightOff() {
        System.out.println("Light is off in the garage door");
    }

}
